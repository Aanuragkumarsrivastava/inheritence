package pack1;

public class Person 
{
		private int id;
		private String name;
		
		
		public Person(int id, String name) {
			super();
			this.id = id;
			this.name = name;
		}

		public int getid()
		{
			return id;
		}
		
		public String getname()
		{
			return name;
		}
}
